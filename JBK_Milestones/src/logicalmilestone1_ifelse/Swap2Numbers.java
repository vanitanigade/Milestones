package logicalmilestone1_ifelse;

public class Swap2Numbers {

	public static void main(String[] args) {

		int original1=10, original2=20;
		int temp;
		
		temp=original1;
		original1=original2;
		original2=temp;
		
		
		System.out.println("after swapping "+original1);
		System.out.println("after swapping "+original2);
		
		
	}

}
