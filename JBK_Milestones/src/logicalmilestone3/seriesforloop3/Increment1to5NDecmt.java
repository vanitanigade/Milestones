package logicalmilestone3.seriesforloop3;
//Q.9 generate the series. 1 2 3 4 5 4 3 2 1

public class Increment1to5NDecmt {

	public static void main(String[] args) {

		for(int i=1; i<=5; i++) {
			System.out.print(i + " ");
		}
		
		for(int j=4; j>=1; j--) {
			System.out.print(j + " ");
		}
	}

}
