package logicalmilestone3.seriesforloop3;

public class MultiplesOf3 {

	public static void main(String[] args) {

		for(int i=3; i<=30; i=i+3) {
			System.out.print(i + " ");
		}

		System.out.println();


		int n=3;
		for(int i=1; i<=10; i++) {
			System.out.print(n*i + " ");
		}
	}

}
