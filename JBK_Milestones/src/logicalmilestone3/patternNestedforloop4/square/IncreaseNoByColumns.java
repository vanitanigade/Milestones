public class IncreaseNoByColumns {
	
	public static void main(String[] args) {

	for(int i=1; i<=3; i++) {
		for(int j=1; j<=5; j++) {
			System.out.print(j + " ");			// if i is written then rows will be printed
		}
		System.out.println();
	}
}

}
